package Javacodeingprograms;

public class Tableprint {
    public static void main(String[] args) {
        System.out.println("multiplication of 2 table");
        for(int i=1;i<=10;i++)
        {
            int result=2*i;
            System.out.printf("2*%2d=%3d%n",result,i);
        }
    }
}
